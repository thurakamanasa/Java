public class Switch2 {

	public static void main(String[] args) {
		String name = "Sonia";
	    
	    switch(name) {
	        
	      case "Rahul":
	        System.out.println("Rahul");
	        break;

	      case "Sonia":
	        System.out.println("Sonia");
	        break;
	            
	      
	      default:
	        System.out.println("Employee Name:---");
	    }
	  }

	}


